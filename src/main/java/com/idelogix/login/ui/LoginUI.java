    private void clearValues() {
        tUserName.setText("");
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(new String[0]);
        cbRoles.setModel(model);
        tPassword.setText("");
    }

    private void fillRolesComboBox() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(new String[0]);
        cbRoles.setModel(model);
        if (!tUserName.getText().equals("")) {
            if (UserService.getInstance().userExists(tUserName.getText())) {
                String[] arr = UserService.getInstance().getUserRoleList(tUserName.getText());
                if (!(arr.length==0)) {
                    model = new DefaultComboBoxModel<>(arr);
                    cbRoles.setModel(model);
                }
            }
        }

    }
