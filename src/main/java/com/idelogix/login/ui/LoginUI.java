    private void bOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bOKActionPerformed
        String thePassword = new String(tPassword.getPassword());

        if (Utils.isUNameValid(tUserName.getText()) //Checks user name min lenght
                && Utils.isPwdValid(thePassword) //Checks password min lenght
                && UserService.getInstance().validateUserLogin(tUserName.getText(), thePassword, (String) cbRoles.getSelectedItem())) {
            //Logged successfuly
            this.setVisible(false);
            MainUI m = new MainUI();
            //Welcome message on info panel.
            m.setInfoText(Props.getInstance().getTxtProps("msg._Welcome") + " " + Globals.getLoggedUserName() + "!");
            //Make sure window is shown on same screen as loging dialog.
            m.setLocationRelativeTo(SwingUtilities.getWindowAncestor((Component) evt.getSource()));
            m.setVisible(true);
            //System.out.println(new UserService().getInstance().getAppUser());
        } else {
            tPassword.setText("");
            JOptionPane.showMessageDialog(this, UIUtils.getLocalText("msg._Wrong_password_or_user_name"));
        }
    }//GEN-LAST:event_bOKActionPerformed

